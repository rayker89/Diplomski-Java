  CREATE OR REPLACE FORCE VIEW "SUNFLOWER"."PROMOTION_USED_TOKEN" ("PROMOTION_ID", "AGREEMENT_ID", "TOKEN_COUNT") AS 
  with promotion_user_token_improved as ( 
  select putn.promotion_id, putn.AGREEMENT_ID, put.TOKEN_COUNT + putn.TOKEN_COUNT as TOKEN_COUNT
  from PARCIAL_PROMOTION_USED_TOKEN put, AGREEMENT_CHANGE ac, PARCIAL_PROMOTION_USED_TOKEN putn
  where put.AGREEMENT_ID = ac.old_agreement_id
  and putn.AGREEMENT_ID = ac.NEW_AGREEMENT_ID
  and putn.promotion_id = put.promotion_id
  union 
  select pu.promotion_id, pu.AGREEMENT_ID, pu.TOKEN_COUNT
  from PARCIAL_PROMOTION_USED_TOKEN pu
  where pu.agreement_id not in (select old_agreement_id from agreement_change)
)
select promotion_id, agreement_id, max(TOKEN_COUNT) token_count
from promotion_user_token_improved
where agreement_id not in (select old_agreement_id from agreement_change)
group by promotion_id, agreement_id
