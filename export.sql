  select last_acc.agreement_id, pro.id as promotion_id, b.id billing_id, b.end_date, last_acc.become_client_from
from (select ac.agreement_id, max(ac.id) accounting_id, min(ad.diary_date) become_client_from 
      from accounting ac, agreement a, AGREEMENT_DIARY ad, client c, agreement a2 
      where ac.agreement_id = a.id  
      and c.id = a.client_id
      and a2.id = ad.agreement_id
      and ad.status = 'STARTED'  
      and a2.client_id = c.id  
      and ac.referenced_accounting_id is null 
      group by ac.agreement_id ) last_acc, promotion_accounting_log pal, promotion pro, accounting acc_ref, billing b 
where last_acc.accounting_id = pal.accounting_id 
and pal.promotion_id = pro.id 
and pro.promotion_template_id in (4, 7) 
and last_acc.accounting_id = acc_ref.referenced_accounting_id
and acc_ref.id = b.accounting_id
and last_acc.agreement_id not in (select old_agreement_id from agreement_package_change)
group by last_acc.agreement_id, pro.id, b.id, last_acc.become_client_from,b.end_date
