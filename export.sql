  with fixed_info as (
                select prom.id promotion_id, prom.name promotion_name, pac.service_id, fpi.token, case when pac.type = 'Pojedinac' then 'INDIVIDUAL' else 'COMPANY' end client_type
                from FIXED_PROMOTION_info fpi, promotion prom, package pac
                where prom.id = fpi.PROMOTION_ID              
                and pac.name = fpi.PACKAGE_NAME 
               -- and fpi.promotion_id = 102
                group by prom.id,  prom.name, pac.service_id, fpi.token, case when pac.type = 'Pojedinac' then 'INDIVIDUAL' else 'COMPANY' end
              ), bundle_service as (
                select fpi.promotion_id, pacbu.service_id
                from FIXED_PROMOTION_info fpi, service s, package pac 
                  left join BUNDLE_PACKAGE bu on bu.parent_package_id = pac.id and bu.leading = 1 
                  left join package pacbu on pacbu.id = bu.child_package_id 
                where pac.name = fpi.PACKAGE_NAME 
                and pacbu.service_id is not null
                group by fpi.promotion_id, pacbu.service_id
              ), fixed_info_service as (
                select fi.promotion_id, fi.promotion_name, fi.client_type, fi.token, coalesce(bs.service_id, fi.service_id)service_id
                from fixed_info fi left join bundle_service bs on bs.promotion_id = fi.promotion_id
                where fi.service_id <> bs.service_id or bs.service_id is null
              )
              select fis.service_id, s.name service_name, fis.client_type, fis.token, prom."ID",prom."NAME",prom."DESCRIPTION",prom."PROMOTION_TEMPLATE_ID",prom."START_DATE",prom."END_DATE",prom."PROMOTION_LEVEL",prom."ORDER_NO",prom."PENALTY_TYPE_ID",prom."AGREEMENT_OBLIGATION_ID",prom."PENALTY_COEFFICIENT",prom."USER_FORM_ID" 
              from fixed_info_service fis, promotion prom, service s
              where fis.promotion_id = prom.id
              and s.id = fis.service_id
